//increment(++) & decrement(--)
//1)increment(++)
let count =0;
count++;

console.log(count);

//decrement(--)

let score=100;
score--;
score--;

console.log(score);

//postfix and prefix
//1)postfix

let m=5;
console.log(m++); //5
console.log(m); //6

//2)prefix

let n =5;
console.log(++n); //6
